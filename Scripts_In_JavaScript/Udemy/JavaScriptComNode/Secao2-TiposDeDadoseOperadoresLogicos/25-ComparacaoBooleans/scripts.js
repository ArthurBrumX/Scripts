// Booleans: Comparacoes

    // maior que: >
    // menor que: <
    // maior ou igual: >=
    // menor ou igual: <=
    // igual: ==
    // Diferente: !=
    // Identico: === (aqui é um teste pra ver nao so o valor mas se o tipo é igual tambem)

console.log(1 > 2); // false
console.log(5 < 10); // true
console.log(3 >= 3); // true
console.log(5 <= 4); // false

console.log('Arthur' != 'Arthur'); // false
console.log(3 === '3'); // false
console.log(4 === 4); //true